package app.test.migrator.matching.util;

public class Quintuple <Fi, S, T, Fo, Fif>{
    public Fi first;
    public S second;
    public T third;
    public Fo fourth;
    public Fif fifth;

    public Quintuple(Fi first, S second, T third, Fo fourth, Fif fifth) {
        this.first = first;
        this.second = second;
        this.third = third;
        this.fourth = fourth;
        this.fifth = fifth;
    }
}
