public class Quadruple <Fi, S, T, Fo>{
    public Fi first;
    public S second;
    public T third;
    public Fo fourth;

    public Quadruple(Fi first, S second, T third, Fo fourth) {
        this.first = first;
        this.second = second;
        this.third = third;
        this.fourth = fourth;
    }
}
